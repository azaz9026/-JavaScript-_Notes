/**
 * Assignment
In JavaScript, the assignment operator (=) is used to assign a value to a variable. It is one of the fundamental operators in the language
 and plays a central role in variable initialization and modification. Here's how the assignment operator works:
 */

 let variableName = 'value';
 console.log(variableName)

 /**
     variableName`: This is the name of the variable to which you want to assign a value. You should have already declared this variable using `let`, `const`, or `var` before assigning a value to it.
     value`: This is the value you want to assign to the variable. It can be a literal value (e.g., a number, string, boolean), the result of an expression, or the value of another variable.
  */

//    Examples of using the assignment operator:

let age = 21; // Assign the value 30 to the variable 'age'
let name = "Azaz"; // Assign the string "Alice" to the variable 'name'
let isStudent = true; // Assign the boolean value 'true' to the variable 'isStudent'

// Assign the result of an expression to a variable
let total = 5 + 3; // Assign the result of 5 + 3 (which is 8) to the variable 'total'

// Assign the value of one variable to another variable
let x = 10;
let y = x; // Assign the value of 'x' (which is 10) to the variable 'y'

console.log(age , name , 
    isStudent , total , x , y)
